p = input('Digite algum caracter: ')
print('Tipo do dado: ', type(p))
print('Número: ', p.isnumeric())
print('Alfabeto: ', p.isalpha())
print('Alfanumérico: ', p.isalnum())
print('Ascii: ', p.isascii())
print('Decimal: ', p.isdecimal())
print('Maiusculo: ', p.isupper())
